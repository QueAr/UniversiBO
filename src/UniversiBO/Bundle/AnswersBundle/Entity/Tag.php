<?php

namespace UniversiBO\Bundle\AnswersBundle\Entity;

use Doctrine\ORM\Mapping as ORM;

/**
 * UniversiBO\Bundle\AnswersBundle\Entity\Tag
 *
 * @ORM\Table(name="ua_tags")
 * @ORM\Entity(repositoryClass="UniversiBO\Bundle\AnswersBundle\Entity\TagRepository")
 */
class Tag
{
    /**
     * @var integer $id
     *
     * @ORM\Column(name="id", type="integer")
     * @ORM\Id
     * @ORM\GeneratedValue(strategy="AUTO")
     */
    private $id;

    /**
     * @var string $name
     *
     * @ORM\Column(name="name", type="string", length=100, unique=true)
     */
    private $name;


    /**
     * Get id
     *
     * @return integer 
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set name
     *
     * @param string $name
     */
    public function setName($name)
    {
        $this->name = $name;
    }

    /**
     * Get name
     *
     * @return string 
     */
    public function getName()
    {
        return $this->name;
    }
}